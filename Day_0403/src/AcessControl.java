
public class AcessControl { // remote controller
	
	private int x = 10;
	private int y = 20;
	
	public void setX(int x) {	// setter
		this.x = x;		// source -> generate getter and setter  선택으로 자동생성 가능
	}
	
	public int getX() {	// getter
		
		return x;
	}
	
	public void setY(int y) {	// setter
		this.y = y;
	}
	
	public int getY() {	// getter
		
		return y;
	}
	
	
	
}
